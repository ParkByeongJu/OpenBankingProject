package kr.ac.kopo.VO;

public class ProductVO {

	private int productcd;
	private String productContent;
	private int productRate;
	private String productName;
	private String productClassification;
	private int productStat;
	
	
	public int getProductcd() {
		return productcd;
	}
	public void setProductcd(int productcd) {
		this.productcd = productcd;
	}
	public String getProductContent() {
		return productContent;
	}
	public void setProductContent(String productContent) {
		this.productContent = productContent;
	}
	public int getProductRate() {
		return productRate;
	}
	public void setProductRate(int productRate) {
		this.productRate = productRate;
	}
	public String getProductName() {
		return productName;
	}
	public void setProductName(String productName) {
		this.productName = productName;
	}
	public String getProductClassification() {
		return productClassification;
	}
	public void setProductClassification(String productClassification) {
		this.productClassification = productClassification;
	}
	
	public int getProductStat() {
		return productStat;
	}
	public void setProductStat(int productStat) {
		this.productStat = productStat;
	}
	
}
